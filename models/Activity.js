const mongoose = require("mongoose");
const { Schema, ObjectId } = mongoose;
const activitySchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  type: {
    type: String,
    required: true,
  },
  imageUrl: {
    type: String,
    required: true,
  },
  isPopular: {
    type: Boolean,
  },
  itemId: {
    type: ObjectId,
    ref: "Item",
  },
});

//export Activity Model so it can be use in anywhere
module.exports = mongoose.model("Activity", activitySchema);
